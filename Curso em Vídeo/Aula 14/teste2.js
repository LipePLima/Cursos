// Estrutura de repetição WHILE
var cont = 1
while (cont <= 6) {
    console.log(`Passo ${cont}`)
    cont += 1
}

// Estrutura de repetição DO WHILE
var cont = 1
do {
    console.log(`Passo ${cont}`)
    cont++
} while (cont <= 6)

// Estrutura de repetição FOR
for (var cont = 1; cont <= 5; cont += 1) {
    console.log(cont)
}

